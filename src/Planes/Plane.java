package Planes;

/**
 * This is an abstract method that all planes in the system will extend
 * @author v8243060
 */
public abstract class Plane {
    
    /**
     * Serial number of the plane
     */
    String serialNumber;
    
    /**
     * Whether the plane has a priority
     */
    boolean priority;
    
    /**
     * Model of the plane
     */
    String model;
    
    /**
     * Company the plane belongs to
     */
    String company;
    
}
